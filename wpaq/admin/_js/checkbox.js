      function check_tb1() {
        var ch = document.getElementById("checkb1");
        if (ch.checked == true) {
          document.getElementById("tb1").style.textDecoration = "line-through";
          document.getElementById("tb1").style.color = "grey";
        }
        else {
          document.getElementById("tb1").style.textDecoration = "none";
          document.getElementById("tb1").style.color = "black";
        }
      }

