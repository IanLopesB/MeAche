      function check_tb1() {
        var checkBox = document.getElementById("checkb1");
        var id = document.getElementById("tb1");

        if (checkBox.checked == true) {
          id.style.textDecoration = "line-through";
          id.style.color = "grey";
        }
        else {
          id.style.textDecoration = "none";
          id.style.color = "black";
        }
      }

